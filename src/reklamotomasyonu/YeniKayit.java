    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        EmailDogrula edgr = new EmailDogrula();
        String adsoyad = adSoyadAlan.getText().trim();
        String adres = adresAlan.getText().trim();
        String telefon = telefonAlan.getText().trim();
        String email = mailAdresiAlan.getText().trim();
        String tckn = tcKimlikAlan.getText().trim();
        String vergi = vergiNoAlan.getText().trim();
        String kuAd = kAdiAlan.getText().trim();
        String sifre = sifreAlan.getText().trim();
        String sehir = String.valueOf(jComboBox1.getSelectedItem()).split("-")[0];
        
        if (!edgr.dogrula(email)) {
            emailHata.setText("* Geçersiz e-mail adresi");
        }else{
             emailHata.setText("");
             String[] gidecek = new String[9];
             gidecek[0] = adsoyad;
             gidecek[1] = adres;
             gidecek[2] = telefon;
             gidecek[3] = email;
             gidecek[4] = sehir;
             gidecek[5] = tckn;
             gidecek[6] = vergi;
             gidecek[7] = kuAd;
             gidecek[8] = sifre;
             if (d.KullaniciEkle(gidecek)) {
                 JOptionPane.showMessageDialog(null, "Kayıt Başarılı Giriş Panelinden Giriş Yapabilirsiniz.");
                Giris g = new Giris();
                g.show();
                this.hide();
            }else{
                 sonuc.setText("Kayıt Başarısız.");
             }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

