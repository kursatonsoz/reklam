    private void telefonAlanKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_telefonAlanKeyTyped
        char karakter = evt.getKeyChar();
        if (((karakter<'0')||(karakter>'9'))) {
            evt.consume();
        }
        if(telefonAlan.getText().length()>9){
            evt.consume();
        }
    }//GEN-LAST:event_telefonAlanKeyTyped

